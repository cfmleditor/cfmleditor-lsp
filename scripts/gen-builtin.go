// +build ignore

package main

import (
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

type Param struct {
	Name        string `json:"name"`
	Description string `json:"description"`
	Type        string `json:"type"`
	Required    bool   `json:"required"`
}

type Entry struct {
	Name        string  `json:"name"`
	Description string  `json:"description"`
	Syntax      string  `json:"syntax"`
	Returns     string  `json:"returns"`
	Type        string  `json:"type"`
	Params      []Param `json:"params"`
}

func main() {
	files, err := filepath.Glob("docs/data/*.json")
	if err != nil || len(files) == 0 {
		fmt.Fprintln(os.Stderr, "no docs/data/*.json files found — run scripts/fetch-docs.sh first")
		os.Exit(1)
	}

	var entries []Entry
	for _, f := range files {
		data, err := os.ReadFile(f)
		if err != nil {
			continue
		}
		var e Entry
		if err := json.Unmarshal(data, &e); err != nil {
			continue
		}
		if e.Type == "function" || e.Type == "tag" {
			entries = append(entries, e)
		}
	}

	sort.Slice(entries, func(i, j int) bool {
		if entries[i].Type != entries[j].Type {
			return entries[i].Type == "tag"
		}
		return strings.ToLower(entries[i].Name) < strings.ToLower(entries[j].Name)
	})

	out, err := os.Create("cfml/builtin_gen.go")
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	defer out.Close()

	fmt.Fprintln(out, "// Code generated by scripts/gen-builtin.go; DO NOT EDIT.")
	fmt.Fprintln(out, "")
	fmt.Fprintln(out, "package cfml")
	fmt.Fprintln(out, "")
	fmt.Fprintln(out, "func builtinEntries() []Entry {")
	fmt.Fprintln(out, "\treturn []Entry{")

	for _, e := range entries {
		fmt.Fprintf(out, "\t\t{Name: %s, Description: %s, Syntax: %s, Returns: %s, Type: %s",
			goStr(e.Name), goStr(e.Description), goStr(e.Syntax), goStr(e.Returns), goStr(e.Type))
		if len(e.Params) > 0 {
			fmt.Fprintln(out, ", Params: []Param{")
			for _, p := range e.Params {
				fmt.Fprintf(out, "\t\t\t{Name: %s, Description: %s, Type: %s, Required: %v},\n",
					goStr(p.Name), goStr(p.Description), goStr(p.Type), p.Required)
			}
			fmt.Fprintln(out, "\t\t}},")
		} else {
			fmt.Fprintln(out, "},")
		}
	}

	fmt.Fprintln(out, "\t}")
	fmt.Fprintln(out, "}")

	fmt.Fprintf(os.Stderr, "generated cfml/builtin_gen.go with %d entries\n", len(entries))
}

func goStr(s string) string {
	b, _ := json.Marshal(s)
	return string(b)
}
